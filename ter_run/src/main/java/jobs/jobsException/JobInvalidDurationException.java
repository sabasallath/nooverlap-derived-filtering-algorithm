package jobs.jobsException;

public final class JobInvalidDurationException extends Exception {

    public JobInvalidDurationException() {
        super();
    }

    public JobInvalidDurationException(String s) {
        super(s);
    }

    public JobInvalidDurationException(String s, Throwable throwable) {
        super(s, throwable);
    }

    public JobInvalidDurationException(Throwable throwable) {
        super(throwable);
    }
}
