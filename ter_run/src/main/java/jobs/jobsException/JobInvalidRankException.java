package jobs.jobsException;

public final class JobInvalidRankException extends Exception {

    public JobInvalidRankException() {
        super();
    }

    public JobInvalidRankException(String s) {
        super(s);
    }

    public JobInvalidRankException(String s, Throwable throwable) {
        super(s, throwable);
    }

    public JobInvalidRankException(Throwable throwable) {
        super(throwable);
    }
}
